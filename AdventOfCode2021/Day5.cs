        internal static void Part1(List<Line> lines)
        {
            var scores = new Dictionary<(int x, int y), int>();
            foreach(var line in lines)
            {
                if(line.x1 == line.x2)
                {
                    var startY = Math.Min(line.y1, line.y2);
                    var endY = Math.Max(line.y1, line.y2);
                    for(var y = startY; y <= endY; y++)
                    {
                        AddScore(scores, line.x1, y);
                    }
                }
                else if(line.y1 == line.y2)
                {
                    var startX = Math.Min(line.x1, line.x2);
                    var endX = Math.Max(line.x1, line.x2);
                    for (var x = startX; x <= endX; x++)
                    {
                        AddScore(scores, x, line.y1);
                    }
                }
            }
            var countOver2 = scores.Where(x => x.Value >= 2).Count();
            Console.WriteLine($"Overlapped Points: {countOver2}");
        }

		internal static void Part2(List<Line> lines)
		{
			var scores = new Dictionary<(int x, int y), int>();
			foreach (var line in lines)
			{
				var xDir = line.x2 >= line.x1 ? 1 : -1;
				var yDir = line.y2 >= line.y1 ? 1 : -1;
				if (line.x1 == line.x2)
				{
					for (var y = line.y1; (line.y2 - y) * yDir >= 0; y += yDir)
					{
						AddScore(scores, line.x1, y);
					}
				}
				else 
				{
					var y = line.y1;
					for (var x = line.x1; (line.x2 - x) * xDir >= 0; x += xDir)
					{
						AddScore(scores, x, y);
						if (line.y1 != line.y2)
                        {
							y += yDir;
                        }
					}
				}
			}
			var countOver2 = scores.Where(x => x.Value >= 2).Count();
			Console.WriteLine($"Overlapped Points: {countOver2}");
		}

		internal static void AddScore(Dictionary<(int x, int y), int> scores, int x, int y)
        {
			if (!scores.ContainsKey((x, y)))
			{
				scores[(x, y)] = 0;
			}
			scores[(x, y)]++;
		}

